-- 회원 정보 테이블
CREATE TABLE member(
	mno		NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,	-- 회원 번호
	id		VARCHAR2(50) NOT NULL UNIQUE,									-- 아이디
	pw		VARCHAR2(50) NOT NULL,											-- 비밀번호
	name	VARCHAR2(50) NOT NULL,											-- 이름
	phone	VARCHAR2(20),													-- 전화번호
	email 	VARCHAR2(100) NOT NULL,											-- 이메일
	addr	VARCHAR2(200),													-- 주소
	info	char(1), 														-- 개인 정보 이용 동의 여부
	status	char(1) DEFAULT 'Y'												-- 계정 활성화 여부(기본: 'Y', 탈퇴: 'N')
);

-- 전체 조회
SELECT * FROM member;

-- 테이블 제거
DROP TABLE member PURGE;

-- 도서 정보 테이블
CREATE TABLE book(
	bno				NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,		-- 도서 번호
	title			VARCHAR2(200) NOT NULL,												-- 제목
	author			VARCHAR2(50) NOT NULL,												-- 저자
	publisher		VARCHAR2(100) NOT NULL,												-- 발행 기관
	p_date			NUMBER(4),															-- 발행 연도
	cover			VARCHAR2(200),														-- 표지 이미지 경로
	is_available	VARCHAR2(20) DEFAULT 'AVAILABLE',									-- 대출 가능 여부(기본: 대출 가능, 대출 중인 경우: 불가능 'UNAVAILABLE')
	b_status		char(1) DEFAULT 'Y'													-- 상태(삭제 시 'N')
);

-- 전체 조회
SELECT * FROM book;

-- 테이블 제거
DROP TABLE book PURGE;

-- 도서 대출 테이블
CREATE TABLE book_loan(
	loan_no			NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,		-- 대출 번호
	bno				NUMBER NOT NULL REFERENCES book(bno),								-- 도서 번호
	user_id			VARCHAR2(50) NOT NULL REFERENCES member(id),						-- 대출자 아이디
	loan_status		VARCHAR2(50) DEFAULT 'BORROWED',									-- 대출 상태(기본: 대출 중, 반납 시 'RETURNED' / 추후 예약 기능 추가 가능)
	borrow_date 	DATE DEFAULT SYSDATE NOT NULL,										-- 대출일
	return_date 	DATE																-- 반납일
);

