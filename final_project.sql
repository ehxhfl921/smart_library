	user_name 		VARCHAR2(50) NOT NULL,											-- 예약자 이름
	reserve_date	DATE NOT NULL,													-- 예약 희망일
	apply_date		DATE DEFAULT SYSDATE NOT NULL,									-- 예약 신청일
	status			VARCHAR2(50) DEFAULT 'PENDING'									-- 예약 상태(기본: 대기 /승인, 거절, 취소) 
);

