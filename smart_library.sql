-- 스터디룸 예약 테이블
CREATE TABLE studyroom_reservation(
	rno		 		NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,	-- 예약 번호
	sno		 		NUMBER NOT NULL REFERENCES studyroom(sno),						-- 스터디룸 번호
	user_id	 		VARCHAR2(50) NOT NULL REFERENCES member(id),					-- 예약자 아이디
	user_name 		VARCHAR2(50) NOT NULL,											-- 예약자 이름
	reserve_date	DATE NOT NULL,													-- 예약 희망일
	apply_date		DATE DEFAULT SYSDATE NOT NULL,									-- 예약 신청일
	status			VARCHAR2(50) DEFAULT 'PENDING'									-- 예약 상태(기본: 대기 /승인, 거절, 취소) 
);

-- 전체 조회
SELECT * FROM studyroom_reservation;

-- 테이블 제거
DROP TABLE studyroom_reservation PURGE;

