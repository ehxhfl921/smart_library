-- 건의 사항 게시글 저장 테이블
CREATE TABLE suggestion(
	sug_no			NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,	-- 건의 게시글 번호
	s_title			VARCHAR2(200) NOT NULL,											-- 제목
	s_userid		VARCHAR2(50) NOT NULL REFERENCES member(id),					-- 작성자 아이디
	s_author		VARCHAR2(50) NOT NULL,											-- 작성자 이름
	s_content		CLOB,															-- 건의 내용
	s_create_date	DATE DEFAULT SYSDATE NOT NULL,									-- 작성 시간
	s_update_date	DATE,															-- 수정 시간
	s_status		char(1) DEFAULT 'Y'												-- 상태(삭제 시 'N')
);

-- 전체 조회
SELECT * FROM suggestion;

-- 테이블 제거
DROP TABLE suggestion PURGE;

